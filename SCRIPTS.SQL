    NOME            VARCHAR(100),
    CPF             FLOAT,
    VL_SALARIO      FLOAT
);
ALTER TABLE FUNCIONARIO ADD CONSTRAINT PK_FUNCIONARIO PRIMARY KEY (ID_FUNCIONARIO);


CREATE TABLE DEPENDENTE (
    ID_DEPENDENTE    INTEGER NOT NULL,
    NOME             VARCHAR(100),
    BO_CALCULA_IR    CHAR(3),
    BO_CALCULA_INSS  CHAR(3),
    ID_FUNCIONARIO   INTEGER NOT NULL
);
ALTER TABLE DEPENDENTE ADD CONSTRAINT PK_DEPENDENTE PRIMARY KEY (ID_DEPENDENTE, ID_FUNCIONARIO);
